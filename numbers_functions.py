def summa_of_two(a: float, b: float) -> float:
    result: float = a + b
    return result


def substruction_of_two(a: float, b: float) -> float:
    result: float = a - b
    return result


def production(a: float, b: float) -> float:
    return a * b


def division(a: float, b: float) -> float:
    return a / b

